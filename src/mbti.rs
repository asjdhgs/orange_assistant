#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn validates_mbti_type() {
        assert!(is_valid_type("INTJ"));
        assert!(is_valid_type("ESFP"));
        assert!(!is_valid_type("XXXX"));
        assert!(!is_valid_type("INTJJ"));
    }

    #[test]
    fn calculates_expected_type() {
        let questions: Vec<Vec<String>> = fallback_questions()
            .into_iter()
            .map(|question| question.as_frontend_row())
            .collect();
        let operation = (0..questions.len())
            .map(|index| (index.to_string(), 1))
            .collect();
        let result =
            calculate_type(&questions, &MbtiChoiceRequest { operation }).expect("valid result");
        assert_eq!(result.len(), 4);
        assert!(is_valid_type(&result));
    }
}
