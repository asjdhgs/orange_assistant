#[derive(Debug, Clone, Serialize)]
pub struct MbtiQuestion {
    pub question: String,
    pub option_a: String,
    pub option_b: String,
    pub dimension: String,
}

impl MbtiQuestion {
    fn as_frontend_row(&self) -> Vec<String> {
        vec![
            self.question.clone(),
            self.option_a.clone(),
            self.option_b.clone(),
            self.dimension.clone(),
        ]
    }
}

pub async fn load_questions(state: &Arc<AppState>) -> AppResult<Vec<Vec<String>>> {
    let mut questions = if let Some(pool) = &state.databases.mbti {
        let rows = sqlx::query(
            "SELECT question_text, option1, option2, dimension \
             FROM mbti_questions ORDER BY RAND() LIMIT 40",
        )
        .fetch_all(pool)
        .await?;
        rows.into_iter()
            .filter_map(|row| {
                Some(MbtiQuestion {
                    question: row.try_get("question_text").ok()?,
                    option_a: row.try_get("option1").ok()?,
                    option_b: row.try_get("option2").ok()?,
                    dimension: row.try_get("dimension").ok()?,
                })
            })
            .collect::<Vec<_>>()
    } else {
        fallback_questions()
    };

    if questions.len() < 40 {
        questions = fallback_questions();
    }
    Ok(questions
        .into_iter()
        .take(40)
        .map(|question| question.as_frontend_row())
        .collect())
}

pub fn calculate_type(questions: &[Vec<String>], request: &MbtiChoiceRequest) -> AppResult<String> {
    if questions.len() < 4 {
        return Err(AppError::Internal("MBTI 题库未正确加载".into()));
    }
    let mut scores: HashMap<char, i32> = [('E', 0), ('S', 0), ('F', 0), ('J', 0)]
        .into_iter()
        .collect();

    for (index_text, choice) in &request.operation {
        let index = index_text
            .parse::<usize>()
            .map_err(|_| AppError::Validation(format!("无效题号：{index_text}")))?;
        let question = questions
            .get(index)
            .ok_or_else(|| AppError::Validation(format!("题号超出范围：{index}")))?;
        if !matches!(choice, 1 | 2) {
            return Err(AppError::Validation(format!(
                "第 {} 题只能选择选项 1 或 2",
                index + 1
            )));
        }
        let dimension = question
            .get(3)
            .and_then(|value| value.chars().next())
            .ok_or_else(|| AppError::Internal("题目缺少 MBTI 维度".into()))?;
        if let Some(score) = scores.get_mut(&dimension) {
            *score += if *choice == 1 { 1 } else { -1 };
        }
    }

    if request.operation.len() < questions.len() {
        return Err(AppError::Validation("请完成全部 MBTI 题目后再提交".into()));
    }
    Ok(format!(
        "{}{}{}{}",
        if scores[&'E'] > 0 { 'E' } else { 'I' },
        if scores[&'S'] > 0 { 'S' } else { 'N' },
        if scores[&'F'] > 0 { 'F' } else { 'T' },
        if scores[&'J'] > 0 { 'J' } else { 'P' },
    ))
}

pub async fn career_recommendation(state: &Arc<AppState>, raw_type: &str) -> AppResult<String> {
    let mbti_type = raw_type.trim().to_ascii_uppercase();
    if !is_valid_type(&mbti_type) {
        return Err(AppError::Validation(
            "MBTI 类型格式无效，应类似 INTJ、ENFP".into(),
        ));
    }

    if let Some(pool) = &state.databases.mbti {
        let rows = sqlx::query(
            "SELECT c.career_name, c.career_description \
             FROM mbti_career_mapping m \
             JOIN mbti_types t ON t.mbti_id = m.mbti_id \
             JOIN careers c ON c.career_id = m.career_id \
             WHERE UPPER(t.mbti_code) = ? \
             ORDER BY m.compatibility_score DESC, m.is_core DESC, c.career_id LIMIT 12",
        )
        .bind(&mbti_type)
        .fetch_all(pool)
        .await;
        if let Ok(rows) = rows {
            let careers: Vec<String> = rows
                .into_iter()
                .filter_map(|row| row.try_get::<String, _>("career_name").ok())
                .collect();
            if !careers.is_empty() {
                return Ok(format_careers(&mbti_type, &careers));
            }
        }
    }

    let careers = fallback_careers(&mbti_type);
    Ok(format_careers(&mbti_type, careers))
}

fn is_valid_type(value: &str) -> bool {
    let bytes = value.as_bytes();
    bytes.len() == 4
        && matches!(bytes[0], b'I' | b'E')
        && matches!(bytes[1], b'N' | b'S')
        && matches!(bytes[2], b'F' | b'T')
        && matches!(bytes[3], b'J' | b'P')
}

fn format_careers(mbti_type: &str, careers: &[impl AsRef<str>]) -> String {
    let names: Vec<&str> = careers.iter().map(|value| value.as_ref()).collect();
    let split = names.len().min(6);
    let preferred = names[..split]
        .iter()
        .map(|name| format!("- **{name}**：{}", career_blurb(name)))
        .collect::<Vec<_>>()
        .join("\n");
    let interested = names[split..]
        .iter()
        .map(|name| format!("- **{name}**：{}", career_blurb(name)))
        .collect::<Vec<_>>()
        .join("\n");
    let extra = if interested.is_empty() {
        String::new()
    } else {
        format!("\n\n## 也可以进一步了解\n\n{interested}")
    };
    format!(
        "## {mbti_type} 类型职业推荐\n\n\
        {mbti_type} 类型通常适合发挥自身认知优势与工作偏好的岗位。以下结果仅作专业探索参考，建议再结合课程兴趣、学科基础和真实职业体验判断。\n\n\
        ## 优先推荐方向\n\n{preferred}{extra}\n\n\
        ## 使用建议\n\n- 先查看这些职业对应的大学专业和核心课程。\n- 再比较自己是否喜欢日常工作内容，而不只看职业名称。\n- MBTI 不能决定人生方向，请把它当作辅助工具。"
    )
}

